    //convert id to rune

    module.exports = function getRuneName(id) {
        if(id === 8100) return '<:Domination:807759960993038387>'
        if(id === 8300) return '<:Inspiration:807759961319800883>'
        if(id === 8000) return '<:Precision:807759961060671499>'
        if(id === 8400) return '<:Resolve:807759964411396116>'
        if(id === 8200) return '<:Sorcery:807759961475252224>'
    }